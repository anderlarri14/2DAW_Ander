function modifColor() {
    var c = document.getElementById("idMyCombo");

    if (c == "red") {
        document.getElementById("a").style.color = "red";
    }if (c == "green") {
        document.getElementById("a").style.color = "green";
    }if (c == "blue"){
        document.getElementById("a").style.color = "blue";
    }
    
}